#include "ft_printf.h"

void	print_d(int num, int *n)
{
	ft_putnbr_fd(num, 1);
	if (num == -2147483648)
		*n = *n + 11;
	else if (num <= 0)
	{
		num = -num;
		*n = *n + 1;
	}
	while (num > 0)
	{	
		num = num / 10;
		*n = *n + 1;
	}
}
