#include "ft_printf.h"

void	print_s(char *s, int *n)
{
	int	c;

	c = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		*n = *n + 6;
	}
	else
	{
		while (s[c] != '\0')
		{
			ft_putchar_fd(s[c], 1);
			c++;
			*n = *n + 1;
		}
	}
}
